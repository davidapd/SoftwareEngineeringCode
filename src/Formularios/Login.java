    private void BtnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIniciarActionPerformed
        // TODO add your handling code here:

        String usuario;
        usuario = jTextUsuario.getText();

        char[] contraseña;
        contraseña = jPasswordContraseña.getPassword();
        String contraseña2 = "";

        for (int i = 0; i < contraseña.length; i++) {

            contraseña2 = contraseña2 + String.valueOf(contraseña[i]);

        }

        // valida si los campos tienen texto
        if (usuario.isEmpty() || contraseña2.isEmpty()) {

            JOptionPane.showMessageDialog(null, "Ingesar Usuario y Contraseña ");
             jTextUsuario.setText(null);
             jPasswordContraseña.setText(null);

        } else {

            ResultSet resultado = baseDatos.consultar("SELECT * FROM USUARIOS_TABLA WHERE  usuario = '" + usuario + "'   AND contrasena_usuario  = '" + contraseña2 + "' ");

            try {

                if (resultado.next()) {

                    MenuPrincipal menuP = new MenuPrincipal();
                    menuP.setVisible(true);
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(null, " Usuario y/o contraseña INCORRECTOS... ");
                    jTextUsuario.setText(null);
                    jPasswordContraseña.setText(null);
                    
                }
            } catch (SQLException e) {
                System.out.println(e);
            }


    }//GEN-LAST:event_BtnIniciarActionPerformed
    }
